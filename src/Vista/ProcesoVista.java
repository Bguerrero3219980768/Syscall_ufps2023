    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        
        this.gestorProcesos = new ProcesoNegocio();
        
        List<Proceso> procesos = gestorProcesos.obtenerProcesos();
        for (Proceso proceso : procesos) {
            this.defaultTableModel.addRow(new Object[]{
                proceso.getNombre(),
                proceso.getPid(),
                proceso.getUsoMemoria(),
                proceso.getTiempoCPU()
            });
            pids.add(proceso.getPid());
        }
        tblProceso.setModel(defaultTableModel);
        
        btnCpu.setEnabled(true);
        btnRam.setEnabled(true);
        txtPid.setEnabled(true);
        btnKill.setEnabled(true);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnIniciarActionPerformed

