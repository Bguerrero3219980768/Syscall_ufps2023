    private void btnKillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKillActionPerformed
        try {
            gestorProcesos.matarProceso(String.valueOf(Integer.parseInt(txtPid.getText())));
            if(pids.contains(txtPid.getText())){
                JOptionPane.showMessageDialog(null, "Process killed successfully");
                this.defaultTableModel = new DefaultTableModel();
                initClass();
                this.btnIniciarActionPerformed(evt);
            }
            else {
                JOptionPane.showMessageDialog(null, "El PID " + txtPid.getText() + " no está en uso.");
            }
        }
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar matar el proceso con PID " + txtPid.getText() + ".");
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Inserte un PID Válido");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnKillActionPerformed

